# Condicional if (condicional si)

numero_1 = 2
numero_2 = 10

# Operadores logicos
# menor <
# menor igual <=
# mayor >
# mayor igual  >=
# igual ==
# diferente !=


if numero_1 < numero_2:
    print("numero_1 es menor numero_2")

numero_1 = 1
numero_2 = 1

if numero_1 <= numero_2:
    print("numero_1 es menor o igual a numero_2")

numero_1 = 7
numero_2 = 7

if numero_1 > numero_2:
    print("numero_1 es mayor a numero_2")
else:
    print("numero_1 no es mayor a numero_2")
